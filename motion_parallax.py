# Imports the graphics module from graphics.py
from graphics import graphics
# Imports random to be used for the random colors for the mountains
import random


def middle_mountain(gui, color_1):
    '''
    This function generates the middle mountain in the background.
    gui: This calls the graphics module.
    color_1: This color is generated by a randomizer in the main function.
    return: the middle mountain.
    '''
    # These x and y values are what determine the speed of which the mountain moves
    x = (gui.mouse_x / 15) + 375
    y = (gui.mouse_y / 15) + 375
    # This prints out the triangular mountain
    gui.triangle(x - 290, y + 375, x - 25, y - 225, x + 275, y + 375, color_1)


def side_mountains(gui, color_2, color_3):
    '''
    This function generates the side mountains in the background.
    gui: This calls the graphics module
    color_2: This color is generated by a randomizer in the main function.
    color_3: This color is generated by a randomizer in the main function.
    return: The side mountains
    '''
    # These x and y values are what determine the speed of which the mountains move
    x = (gui.mouse_x/8) + 325
    y = (gui.mouse_y/8) + 325
    # These print out the mountains
    gui.triangle(x - 525, y + 375, x - 175, y - 125, x + 175, y + 375, color_2)
    gui.triangle(x + 525, y + 375, x + 175, y - 125, x - 225, y + 375, color_3)


def sun(gui):
    '''
    This function generates the sun all the way in the back
    gui: This calls the graphics module
    return: The sun
    '''
    # These x and y values are what determine the speed of which the sun moves
    x = (gui.mouse_x/20) + 375
    y = (gui.mouse_y/20) + 375
    # This prints out the sun
    gui.ellipse(x + 150, y - 290, 100, 100, "yellow")


def foreground(gui):
    '''
    This function generates the grass blades, the grass plain, the tree and birds.
    gui: calls the graphics module
    return: The grass blades, grass plain, tree, and birds
    '''
    # These x and y values are what determine the speed of which the foreground moves
    # besides the birds
    x = (gui.mouse_x/4) + 250
    y = (gui.mouse_y/4) + 250
    # This prints out grass plain
    gui.rectangle(x - 500, y + 275, 1000, 300, "green")
    # This while loop prints each blade of grass
    i = -100
    while i <= 900:
        gui.line((x - 375) + i, y + 225, (x - 375) + i, y + 275, "green", 5)
        i += 10
    # These print out the tree
    gui.line(x + 125, y + 125, x + 125, y + 325, "brown", 40)
    gui.ellipse(x + 125, y + 175, 100, 150, "green")
    # This while loop prints out the birds
    index = -50
    while index <= 500:
        gui.line(0 + index, 15 + (index / 3), 25 + index, 30 + (index / 3), "black", 5)
        gui.line(24 + index, 30 + (index / 3), 49 + index, 15 + (index / 3), "black", 5)
        index += 100


