def main():
    gui = graphics(750, 750, 'Motion Parallax')
    # This code generates the random colors that each individual mountain will be
    color_1 = gui.get_color_string(random.randint(0, 255), random.randint(0, 255),
                                   random.randint(0, 255))
    color_2 = gui.get_color_string(random.randint(0, 255), random.randint(0, 255),
                                   random.randint(0, 255))
    color_3 = gui.get_color_string(random.randint(0, 255), random.randint(0, 255),
                                   random.randint(0, 255))
    # This displays the entire drawing
    while True:
        gui.clear()
        gui.rectangle(-50, -50, 800, 800, 'light blue')
        sun(gui)
        middle_mountain(gui, color_1)
        side_mountains(gui, color_2, color_3)
        foreground(gui)
        monkey(gui)
        gui.update_frame(60)


main()
