    gui.ellipse(x - 50, y + 250, 40, 60, brown)
    gui.ellipse(x - 50, y + 250, 27, 45, light_brown)
    gui.line(x - 35, y + 235, x - 15, y + 215, brown, 5)
    gui.line(x - 65, y + 235, x - 80, y + 255, brown, 5)
    gui.line(x - 60, y + 275, x - 70, y + 290, brown, 5)
    gui.line(x - 70, y + 289, x - 68, y + 305, brown, 5)
    gui.line(x - 40, y + 275, x - 30, y + 290, brown, 5)
    gui.line(x - 30, y + 289, x - 32, y + 305, brown, 5)
